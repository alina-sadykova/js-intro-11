// Simple ones + - * /

let num1 = 2;
let num2 = 5;
let num3 = 3;
let num4 = 6;

console.log(num4 / num1); //3
console.log(num1 * num2 * num3); //30

// Exponentiation
console.log(3 ** 2); //9
console.log(4 ** 3); //64
console.log(5 ** 4); //625

console.log(2 ** 5 / 4 + 3 * 2); //14

//Modulus (remainder)

console.log(4 % 5); //4
console.log(40 % 5); //0
console.log(9 % 2); //1

console.log(15 % 4); //3
console.log(((2 * 5) % 3) - 7); //-6
